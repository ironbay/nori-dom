  ]
  |> Enum.each(fn name ->
    type = Atom.to_string(name)

    def unquote(name)(attributes \\ [], body \\ []) do
      Nori.element(
        unquote(type),
        attributes,
        body
      )
    end
  end)

  def style(input) do
    input
    |> Enum.map(fn {key, value} -> [Atom.to_string(key), ": ", value, ";"] end)
    |> IO.iodata_to_binary()
  end

  def class(input) do
    input
    |> Stream.filter(fn {_, value} -> value end)
    |> Enum.map(fn {key, value} -> [Atom.to_string(key), ": ", value] end)
    |> IO.iodata_to_binary()
  end
end
